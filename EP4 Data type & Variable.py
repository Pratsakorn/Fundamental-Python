#ชื่อตัวแปร = ค่าที่จะเก็บข้างใน
x=2
y=3.99
z=True
#เรียกค่าxออกมาใช้
print(x)
#เรียกค่าyออกมาใช้
print(y)
#เรียกค่าzออกมาใช้
print(z)
#string+string
print('ผลลัพธ์\t'+ 'x')
#เปลี่ยนชนิดข้อมูลจากintเป็นstring
print('ผลลัพธ์\t'+ str(x))
#เปลี่ยนชนิดข้อมูลจากfloatเป็นstring
print('ผลลัพธ์\t'+ str(y))
#เปลี่ยนชนิดข้อมูลจากboolเป็นstring
print('ผลลัพธ์\t'+ str(z))
#เรียกดูชนิดข้อมูล
print(type(x))
print(type(y))
print(type(z))
print(type(2))
print(type(3.99))
print(type(True))