#ตัวดำเนินการเปรียบเทียบ
x,y=int(input("first value\n")),int(input("second value\n"))
print("xมากกว่าyหรอ",x>y)
print("xน้อยกว่าyหรอ",x<y)
print("xเท่ากับyหรอ",x==y)
print("xไม่เท่ากับyหรอ",x!=y)
print("xมากกว่า หรือ เท่ากับyหรอ",x>=y)
print("xน้อยกว่า หรือ เท่ากับyหรอ",x<=y)



